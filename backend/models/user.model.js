import Mongoose from "mongoose";

const { Schema } = Mongoose;

const userSchema = Schema(
	{
		name: {
			type: String,
			required: true,
		},
		email: {
			type: String,
			required: true,
			unique: true,
		},
		password: {
			type: String,
			required: true,
		},
	},
	{ timestamps: true }
);

export default Mongoose.model("User", userSchema);
